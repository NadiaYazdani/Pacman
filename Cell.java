class Cell {
    private int row , col ;

    Cell (int r, int c) {
        this.row = r ;
        this.col = c ;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public void setCol(int col) {
        this.col = col;
    }

}